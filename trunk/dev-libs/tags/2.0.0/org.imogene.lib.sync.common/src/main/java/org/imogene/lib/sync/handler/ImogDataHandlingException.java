package org.imogene.lib.sync.handler;

public class ImogDataHandlingException extends Exception {

	/**
	 * Serial Version
	 */
	private static final long serialVersionUID = -7518330780551014605L;

	public ImogDataHandlingException() {
		super();
	}

	public ImogDataHandlingException(String message, Throwable cause) {
		super(message, cause);
	}

	public ImogDataHandlingException(String message) {
		super(message);
	}

	public ImogDataHandlingException(Throwable cause) {
		super(cause);
	}

}
